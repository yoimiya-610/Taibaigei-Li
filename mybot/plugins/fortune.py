async def send_fortune(matcher, bot: Bot, event: MessageEvent):
    # 私聊不支持
    if not isinstance(event, GroupMessageEvent):
        await matcher.finish("📢 签到请在群里使用哦~")
    
    user_id = event.get_user_id()
    group_id = str(event.group_id)
    today = datetime.now().strftime("%Y-%m-%d")
    key = f"{today}_{group_id}_{user_id}"
    
    nickname = await get_nickname(bot, user_id, context="运势")
    
    # 获取今日固定运势（全局一致）
    daily = get_daily_fortune(user_id)

    def record_fortune(data: dict) -> dict:
        data.setdefault("fortune_record", {})
        data.setdefault("streak", {})

        if key in data["fortune_record"]:
            return {"already": True, "result": data["fortune_record"][key]}

        # 计算积分
        base_points = FORTUNE_POINTS[daily["fortune_name"]]
        total_points = base_points

        data["fortune_record"][key] = {
            "fortune": daily["fortune_name"],
            "emoji": daily["emoji"],
            "desc": daily["desc"],
            "special_event": f"{daily['special_event']['name']}（{daily['special_event']['desc']}）" if daily["special_event"] else None
        }

        # 连续签到奖励
        streak_count = check_streak(data, user_id, group_id, today)
        streak_bonus = get_streak_bonus(streak_count)
        total_points += streak_bonus

        # 特殊事件积分
        special_text = ""
        if daily["special_event"]:
            event_info = daily["special_event"]
            if event_info["name"] == "🌈 好运加倍":
                total_points *= 2
                special_text = f"\n✨ 特殊事件：{event_info['name']}（{event_info['desc']}）"
            else:
                total_points += event_info["points"]
                special_text = f"\n✨ 特殊事件：{event_info['name']}（{event_info['desc']}）"

        return {
            "already": False,
            "total_points": total_points,
            "special_text": special_text,
            "streak_count": streak_count,
        }

    fortune_state = mutate_json(DATA_FILE, DEFAULT_FORTUNE_DATA, record_fortune)

    # 检查今天是否已经在本群抽过
    if fortune_state["already"]:
        result = fortune_state["result"]
        special_text = ""
        if result.get("special_event"):
            special_text = f"\n✨ 特殊事件：{result['special_event']}"
        await matcher.finish(
            f"『{nickname}』你今天在本群已经抽过签了！\n"
            f"莫要贪心，一日一签，天道有常。\n\n"
            f"【今日运势】{result['fortune']} {result['emoji']}{special_text}\n"
            f"【运势解读】{result['desc']}"
        )

    total_points = fortune_state["total_points"]
    special_text = fortune_state["special_text"]
    streak_count = fortune_state["streak_count"]

    # 调用积分系统添加积分（分群）
    add_points(user_id, group_id, total_points)

    unknown_notice = ""
    hidden_title_text = ""
    is_unknown_fortune = daily["fortune_name"] == UNKNOWN_FORTUNE_NAME
    if is_unknown_fortune:
        hidden_title = grant_title(user_id, group_id, UNKNOWN_FORTUNE_TITLE_ID)
        hidden_title_text = f"\n🎖 天外名号：{hidden_title.title} 已入册" if hidden_title else ""
        unknown_notice = (
            "\n（本猫酒壶当场落地）全体看签！"
            "这不是签册翻页，这是天机掀桌。"
        )

    # 构建输出
    output = f"""🎋 李太白给·今日运势签 🎋
{'='*20}
✦ {nickname} の运势 ✦
{'='*20}{unknown_notice}
【运势】{daily['fortune_name']} {daily['emoji']}{special_text}
【解读】{daily['desc']}
{'='*20}
🔢 幸运数字：{daily['lucky_number']}
🎨 幸运颜色：{daily['lucky_color']}
✅ 宜：{' / '.join(daily['yi'])}
❌ 忌：{' / '.join(daily['ji'])}
{'='*20}
📜 诗人曰：
{daily['poet_comment']}

『{daily['poem']}』
{'='*20}
🔥 连续签到 {streak_count} 天
💰 今日获得 {total_points} 积分{hidden_title_text}"""

    if is_unknown_fortune:
        await matcher.finish(MessageSegment.at("all") + MessageSegment.text("\n" + output))

    await matcher.finish(output)


