def _makeup_usage_key(year: int, user_id: str, group_id: str) -> str:
    return f"{year}_{group_id}_{user_id}"


def calculate_makeup_cost(makeup_count: int) -> int:
    return MIN_MAKEUP_COST * (2 ** max(makeup_count - 1, 0))


def _find_makeup_target_date(data: dict, user_id: str, group_id: str, today_date: date) -> str:
    year_start = today_date.replace(month=1, day=1)
    cursor = today_date - timedelta(days=1)
    while cursor >= year_start:
        date_text = cursor.strftime("%Y-%m-%d")
        if not _has_signed_on(data, user_id, group_id, date_text):
            return date_text
        cursor -= timedelta(days=1)
    return ""


def _get_makeup_plan(data: dict, user_id: str, group_id: str) -> dict:
    now = datetime.now()
    target_date = _find_makeup_target_date(data, user_id, group_id, now.date())
    if not target_date:
        return {"status": "none", "year": now.year}

    usage_key = _makeup_usage_key(now.year, user_id, group_id)
    usage = data.get("makeup_usage", {}).get(usage_key, {})
    used_count = int(usage.get("count", 0))
    next_count = used_count + 1
    return {
        "status": "available",
        "target_date": target_date,
        "year": now.year,
        "used_count": used_count,
        "next_count": next_count,
        "cost": calculate_makeup_cost(next_count),
    }


def get_makeup_plan(user_id: str, group_id: str) -> dict:
    return _get_makeup_plan(load_data(), user_id, group_id)


def _apply_makeup(user_id: str, group_id: str, expected_cost: int) -> dict:
    def mutator(data: dict) -> dict:
        plan = _get_makeup_plan(data, user_id, group_id)
        if plan["status"] != "available":
            return plan
        if plan["cost"] != expected_cost:
            return {**plan, "status": "cost_changed"}

        record_key = _streak_key(user_id, group_id)
        records = data.setdefault("makeup_records", {}).setdefault(record_key, {"dates": []})
        records.setdefault("dates", []).append(plan["target_date"])

        usage_key = _makeup_usage_key(plan["year"], user_id, group_id)
        data.setdefault("makeup_usage", {})[usage_key] = {"count": plan["next_count"]}
        streak_count = _sync_streak(data, user_id, group_id)
        return {**plan, "status": "created", "streak_count": streak_count}

    return mutate_json(DATA_FILE, DEFAULT_FORTUNE_DATA, mutator)

